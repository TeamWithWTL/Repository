package com.jcwx.entity.pub;

/**
 * @author Gs
 * 2018年5月17日
 * 带参坐标类
 */
public class Point {
	
	
	private double lon;			// 经度
    private double lat;			// 维度
	
	public double getLon() {
		return lon;
	}

	public void setLon(double lon) {
		this.lon = lon;
	}

	public double getLat() {
		return lat;
	}

	public void setLat(double lat) {
		this.lat = lat;
	}
}
